public class DataHolder {
    private String mTitle = null;
    private String mSubTitle = null;

    public DataHolder(String mTitle, String mSubTitle) {
        this.mTitle = mTitle;
        this.mSubTitle = mSubTitle;
    }

    public String getTitle() {
        return mTitle;
    }



    public String getSubTitle() {
        return mSubTitle;
    }


}
